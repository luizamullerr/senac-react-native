function somarop(n1: number, n2: number): number {
    return n1 + n2
}
function subtrairop(n1: number, n2: number): number {
    return n1 - n2
}
function multiplicarop(n1: number, n2: number): number {
    return n1 * n2
}
function dividirop(n1: number, n2: number): number {
    return n1 / n2
}

export { somarop, subtrairop, multiplicarop, dividirop }